    private void buscar() {
        this.bd.Conectar();
        try {
            this.bd.setResult("SELECT cedula FROM empleados WHERE cedula = '" + this.txtCedula.getText() + "'");
            ResultSet result = this.bd.getResult();
            if(result.next()){
              this.bd.setResult("SELECT Pregunta, Respuesta FROM preguntas_seguridad WHERE Cedula_empleado = '" + this.txtCedula.getText() + "'");
              result = this.bd.getResult();
              String[] Preguntas = new String[2];
              
              int i = 0;
              while(result.next()){
                  Preguntas[i] = result.getString(1);
                  this.Respuestas[i] = result.getString(2);
                  i++;
              }
              this.lblPrg1.setText(Preguntas[0]);
              this.lblPrg2.setText(Preguntas[1]);
              this.txtPgt1.setEditable(true);
              this.txtPgt1.setEnabled(true);
              this.txtPgt2.setEditable(true);
              this.txtPgt2.setEnabled(true);
              this.txtPgt1.requestFocus();
              
            }else{
                JOptionPane.showMessageDialog(null, "No existe la cedula ingresada!!!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(OlvidoContrasena.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void guardar() {
        String Contrasena = Encriptacion.Encriptar(String.valueOf(this.txtContrasena.getPassword()));
        
        this.bd.Conectar();
        try {
            this.bd.setStm("UPTADE usuarios SET contrasena = '" + Contrasena +"' WHERE cedula_empleado = '" + this.txtCedula.getText() + "' ");
        } catch (SQLException ex) {
            Logger.getLogger(OlvidoContrasena.class.getName()).log(Level.SEVERE, null, ex);
        }
 
        
        
    }

    private void consultar() {
        if(this.txtPgt1.getText().equalsIgnoreCase(this.Respuestas[0]) && this.txtPgt2.getText().equalsIgnoreCase(this.Respuestas[1])){
            this.txtContrasena.setEditable(true);
            this.txtContrasena.setEnabled(true);
            this.txtVerifContrasena.setEditable(true);
            this.txtVerifContrasena.setEnabled(true);
            this.txtContrasena.requestFocus();
        }else{
            JOptionPane.showMessageDialog(null, "Las respuestas no son las correctas!!");
        }
    }
