    private void imprimir() {
               
           this.bd.Conectar();
        
           
       try{
           String ubicacion = new File("src/Reportes_Luncheria/Reportes_usuario.jasper").getAbsolutePath();
            System.out.println(ubicacion);
       JasperPrint jasperPrintWindow = JasperFillManager.fillReport(ubicacion, null, this.bd.getCon());
        JasperViewer jasperViewer = new JasperViewer(jasperPrintWindow,false);

	jasperViewer.setVisible(true);
        BitacoraVentanas.LOG_RAIZ.log(Level.INFO, "Reporte generado correctamente");
    
       } catch (Exception ex) {
           BitacoraVentanas.LOG_RAIZ.log(Level.SEVERE, "Error al cargar el reporte de cliente");
           JOptionPane.showMessageDialog(this, "Error al cargar el reporte: " + ex, "Error", JOptionPane.ERROR_MESSAGE);
    }
    }

    private void Cargar_Tabla() {
        try{
            this.bd.Conectar();
            this.bd.setResult("SELECT * FROM empleados");
            String Datos[] = new String[8];
            ResultSet result = this.bd.getResult();
            while(result.next()){
                Datos[0] = (""+result.getString(1));
                Datos[1] = (""+result.getString(2));
                Datos[2] = (""+result.getString(3));
                Datos[3] = (""+result.getString(4));
                Datos[4] = (""+result.getString(5));
                switch(result.getString(6)){
                    case "1" : Datos[5] = "MOVIL"; break;
                    case "2" : Datos[5] = "CASA"; break;
                } 
                switch(result.getString(7)){
                    case "M" : Datos[6] = "Masculino"; break;
                    case "F" : Datos[6] = "Femenino"; break;
                }          
                switch(result.getString(8)){
                    case "1" : Datos[7] = "Activo"; break;
                    case "0" : Datos[7] = "Inactivo"; break;
                }          
               

                this.tabla.addRow(Datos);
            }
            //this.bd.Close();
        }catch (SQLException ex) {
                BitacoraVentanas.LOG_RAIZ.log(Level.CONFIG, "Error al cargar la tabla desde la base de datos:{0}", ex.getMessage());
            }
    }


